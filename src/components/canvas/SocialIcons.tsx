export function SocialIconBtn({ platform, url, color, size = 16 }: {
  platform: Platform;
  url: string;
  color: string;
  size?: number;
}) {
  const [hov, setHov] = useState(false);
  const safeUrl = url.startsWith("http") ? url : `https://${url}`;
  const platColor = PLATFORM_COLORS[platform] ?? color;

  return (
    <a
      href={safeUrl}
      target="_blank"
      rel="noopener noreferrer"
      onClick={e => e.stopPropagation()}
      onMouseEnter={() => setHov(true)}
      onMouseLeave={() => setHov(false)}
      title={PLATFORM_LABELS[platform]}
      style={{
        display:        "flex",
        alignItems:     "center",
        justifyContent: "center",
        width:          size + 10,
        height:         size + 10,
        borderRadius:   "50%",
        background:     hov ? `${platColor}26` : "transparent",
        transition:     "all 0.14s ease",
        transform:      hov ? "translateY(-2px)" : "translateY(0)",
        opacity:        hov ? 1 : 0.68,
        textDecoration: "none",
        flexShrink:     0,
      }}
    >
      <PlatformIcon platform={platform} size={size} color={hov ? platColor : color} />
    </a>
  );
}
